"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { cn } from "@/lib/utils";
import Image from "next/image";
import { NowPlaying } from "./now-playing";
import { SessionNavItem } from "./session-nav-item";

const navItems = [
  { href: "/sessions", label: "Sessions", icon: MicIcon },
  { href: "/playlist-parser", label: "Spotify + Suno", icon: MusicIcon },
  { href: "/mix-agent", label: "Mix Agent", icon: SlidersIcon },
  { href: "/inbox", label: "Inbox", icon: InboxIcon },
  { href: "/settings", label: "Settings", icon: SettingsIcon },
];

export function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="w-64 border-r bg-sidebar/90 text-sidebar-foreground flex flex-col h-full backdrop-blur-sm">
      <div className="pt-[60px]" />
      <nav className="flex-1 p-3 space-y-1">
        {navItems.map((item) => {
          if (item.href === "/sessions") {
            return <SessionNavItem key={item.href} icon={item.icon} />;
          }
          const isActive =
            item.href === "/"
              ? pathname === "/"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors",
                isActive
                  ? "bg-sidebar-accent text-sidebar-accent-foreground"
                  : "text-sidebar-foreground/70 hover:bg-sidebar-accent/50 hover:text-sidebar-accent-foreground"
              )}
            >
              {item.href === "/playlist-parser" ? (
                <Image src="/spotify-suno-logo.png" alt="Spotify + Suno" width={67} height={67} className="rounded shrink-0" />
              ) : (
                <item.icon className="h-4 w-4" />
              )}
              {item.label}
            </Link>
          );
        })}
      </nav>
      <div className="border-t">
        <NowPlaying />
      </div>
    </aside>
  );
}

